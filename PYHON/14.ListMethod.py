a=[1,2,3,4,5,7,0,11,9]

#Sort method
a.sort()
print("Sort list:",a)

#Reverse method
a.reverse()
print("Reverse list:",a)

#Append method
a.append(100)
print("Append list:",a)

#Insert list
a.insert(2,77)
print("Insert list:",a)

#Pop method
a.pop(0)
print("Pop List ",a)

#Remove method
a.remove(100)
print("Remove list",a)