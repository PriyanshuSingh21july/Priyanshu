import java.io.PrintStream;

public class Assignment {

               public static void main(String[] args) {
                              PrintStream out = System.out;
                              out.println("**************************Assignment operato (=,)**********************r");
                                /*    =,  += , -= , *= , /= , %=  */

                                int num=10;


                                /* 1. = */
                                int b=num;
                                out.println("Value of b :"+b); /* 10 */

                                 /* 2. += */
                                 int c=6;
                                 c+=num;
                                 out.println("Value of b :"+c); /* 16 */

                                   /* 3. -= */
                                   c-=num;
                                   out.println("Value of b :"+c); /* 6 */
  

                              /* 4. *= */
                                c*=num;
                                 out.println("Value of b :"+c); /* 60 */

                              /* 5. / = */
                                c/=num;
                                out.println("Value of b :"+c); /* 6 */
                                

                                  /* 6. % = */
                                  c/=num;
                                  out.println("Value of b :"+c); /* 6 */
  








                                              }
               
}
