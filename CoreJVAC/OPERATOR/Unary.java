public class Unary {

               public static void main(String[] args) {
                              /* - , + , ++ , -- ,! , &  */

                              int a=10;
                              int b=12;
                              ++a;
                              int b1=b++;
                              System.out.println("Pre increment :"+a);
                              System.out.println("Post Increment :"+b+" "+b1);


                            int c=10;
                            int d=15;
                           int d1= --c;
                            d--;
                            System.out.println("Pre decrement :"+c);
                            System.out.println("Post Increment:"+d1);
               }
               
}


