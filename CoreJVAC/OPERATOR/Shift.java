public class Shift {

               public static void main(String[] args) {
                              
/* >>  <<  >>> */
                              int number = 2;
 
        // 2 bit left shift operation
        int Ans = number << 2;
 
        System.out.println(Ans);


        // 2 bit right shift operation
         Ans = number >>2;
 
        System.out.println(Ans);




               }
               
}
