public class MathFunction {
               public static void main(String[] args) {

                              /* Power */
                              System.out.println("Power of number :"+Math.pow(2, 03));

                              /*Sqrt */
                              System.out.println("Squre of number :"+Math.sqrt(9));

                              /* Max */
                              System.out.println("maximum element :"+Math.max(20,54));

                              /* Min  */
                               System.out.println("minimum element :"+Math.min(20,54));

                               /* abs */
                               System.out.println("Abs :"+Math.abs(5));


                               /* FLoor */
                                System.out.println("Floor value :"+Math.floor(20.9));

                                /* ceal  */
                                   System.out.println("Floor value :"+Math.ceil(20.9));

                              /* round */
                                      System.out.println("Floor value :"+Math.round(20.9));


               }
               
}
